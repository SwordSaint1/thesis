     */
    public function update(Request $request, $id)
    {
        $user = User::find($id);

        $request->validate([
            'name' => ['required', 'string', 'max:255'],
            'email' => ['required', 'string', 'email', 'max:255', 'unique:users,name,' . $user->id],
            'password' => ['nullable', 'string', 'min:8'],
        ]);

        if (is_null($request->password)) {
            $update = $user->update(['name' => $request->name, 'email' => $request->email]);
        } else {
            $update = $user->update(['name' => $request->name, 'email' => $request->email, 'password' =>  Hash::make($request->password)]);
        }


        return redirect()->back()->with('success', 'User updated successfully');
    }

